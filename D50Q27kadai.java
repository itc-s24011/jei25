public class D50Q27kadai {
    public static void main(String[] args) {
        int Control = 1; // Controlの値を設定（例：1）

        switch (Control) {
            case 1:
                System.out.println("Warm");
                break;
            case 2:
                System.out.println("Cool");
                break;
            case 3:
                System.out.println("Wind");
                break;
            default:
                System.out.println("Invalid Control");
        }
    }
}
